//Conversion de variables

//--->Convercion de entero a cadena de texto
let edad = 20;
console.log(edad);
console.log(typeof(edad));


let xedad = String(edad);
console.log(typeof(xedad));

//--->Convercion de string a entero
let cantidad = "500";
console.log(cantidad);
console.log(typeof(cantidad));

let xcantidad = parseInt(cantidad);
console.log(typeof(xcantidad));

//--->Convercion de string a decimal
let precio = "256.30";
console.log(precio);
console.log(typeof(precio));

console.log(typeof(precio));
console.log(typeof(parseFloat(precio)));


