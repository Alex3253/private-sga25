//Desarrollar una aplicacion que permite solicitar dos numeros por la ventana emergente, para luego mostar la suma de los numeros mediante la ventana emergente

let num1, num2, resultado;


num1 = parseFloat(prompt("Ingrese el primer numero: "));
num2 = parseFloat(prompt("Ingrese el segundo numero: "));

resultado= num1 + num2;

alert("la suma de los numeros es " + resultado);



